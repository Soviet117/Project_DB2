  CREATE OR REPLACE NONEDITIONABLE PROCEDURE "ADIXON"."LOAD_HORARIO_DIS" (
	cur OUT SYS_REFCURSOR,
	id_al IN NUMBER)
IS
	id_ci ADIXON.ALUMNO.ID_CICLO%TYPE;
	id_ca ADIXON.ALUMNO.ID_CARRERA%TYPE;
BEGIN
	SELECT ID_CICLO INTO id_ci FROM ADIXON.ALUMNO WHERE ID_ALUMNO = 1;
	SELECT ID_CARRERA INTO id_ca FROM ADIXON.ALUMNO WHERE ID_ALUMNO = 1;
	
	OPEN cur FOR
	SELECT *
	FROM ADIXON.VIEW_HORARIO_DISPO h 
	WHERE h.ID_HORARIO NOT IN (
		SELECT mh.ID_HORARIO 
			FROM ADIXON.MATRICULA_HORARIO mh JOIN ADIXON.MATRICULA m ON mh.ID_MATRICULA = m.ID_MATRICULA
        WHERE
            m.ID_ALUMNO = id_al
    ) AND h.ID_CICLO = id_ci AND (h.ID_CARRERA = 1 OR h.ID_CARRERA = id_ca);
END;

