  CREATE OR REPLACE NONEDITIONABLE FUNCTION "ADIXON"."LOAD_ID_MATRICULA" (
	id_al IN NUMBER)
	RETURN NUMBER
IS
	id_ma ADIXON.MATRICULA.ID_MATRICULA%TYPE;
BEGIN
	SELECT m.ID_MATRICULA INTO id_ma 
	FROM ADIXON.ALUMNO a INNER JOIN ADIXON.MATRICULA m ON m.ID_ALUMNO  = a.ID_ALUMNO
		WHERE a.ID_ALUMNO = id_al;
	RETURN id_ma;

END;

