  CREATE OR REPLACE NONEDITIONABLE PROCEDURE "ADIXON"."DELETE_MATRICULA_HORARIO" (
	id_al IN NUMBER,
	id_horario IN NUMBER,
	id_curso IN NUMBER)
AS
	id_ma ADIXON.MATRICULA.ID_MATRICULA%TYPE;
BEGIN
	id_ma := ADIXON.LOAD_ID_MATRICULA(id_al);
	DELETE FROM ADIXON.MATRICULA_HORARIO WHERE ID_MATRICULA = id_ma AND ID_HORARIO = id_horario;
	DELETE FROM ADIXON.NOTAS WHERE ID_ALUMNO = id_al AND ID_CURSO = id_curso;
END;

