  CREATE OR REPLACE NONEDITIONABLE PROCEDURE "ADIXON"."LOAD_ALUMNOS" (
	cur OUT SYS_REFCURSOR,
	id_al IN NUMBER)
AS
BEGIN
	OPEN cur FOR
    SELECT a.id_alumno, p.id_persona , a.codigo, p.dni, p.apellidos, p.nombres, p.fecha_n, p.telofono, p.correo, p.direccion,a.fecha_ingre, a.password,p.estado ,a.id_ciclo, a.id_carrera 
            FROM ADIXON.alumno a INNER JOIN ADIXON.persona p ON a.id_persona = p.id_persona  
            WHERE a.id_alumno = id_al;
END;

